func TestFernet_TamperedToken(t *testing.T) {
	key, _ := GenerateKey()
	f, _ := NewFernet(key)

	token, _ := f.EncryptString("secret")

	// Tamper with token (flip a character)
	tampered := []byte(token)
	if tampered[20] == 'A' {
		tampered[20] = 'B'
	} else {
		tampered[20] = 'A'
	}

	_, err := f.DecryptString(string(tampered))
	if err == nil {
		t.Error("tampered token should fail HMAC verification")
	}
}

